/* 
 * bitAnd - x&y using only ~ and | 
 *   Example: bitAnd(6, 5) = 4
 *   Legal ops: ~ |
 *   Max ops: 8
 *   Rating: 1
 */
int bitAnd(int x, int y) {
  return ~(~x | ~y);
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
    int bit_per_byte = n << 3;
	int mask = 255;
	int get_byte = (x >> bit_per_byte) & mask;
	return get_byte;
}
/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */
int logicalShift(int x, int n) {
	int minus_n = (~n) + 1;
	int minus_one = ~0;
	int pre_mask = (1 << (32 + minus_n)) & (~1); // case n=0: makes pre_mask 0
	int mask = pre_mask + minus_one;
	return (x >> n) & mask;
}
/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
  int b16, b8, b4, b2, b1;
  int bang;

  b16 = (x >> 16) | x;
  b8 = (b16 >> 8) | b16;
  b4 = (b8 >> 4) | b8;
  b2 = (b4 >> 2) | b4;
  b1 = (b2 >> 1) | b2;
  bang = 1 & (b1 + 1);
  return bang;
}
/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
  int neg_mask = (x >> 31); // neg = -1, pos = 0
  int x_pos = x ^ neg_mask;
  int is_zero = !x_pos;
  int zero_mask = (((!(!x_pos))<<31)>>31);
  int b16, b8, b4, b2, b1;

  b16 = !(!(x_pos >> 16)) << 4;
  x_pos = x_pos >> b16;
  b8 = !(!(x_pos >> 8)) << 3;  
  x_pos = x_pos >> b8;
  b4 = !(!(x_pos >> 4)) << 2;
  x_pos = x_pos >> b4;
  b2 = !(!(x_pos >> 2)) << 1;
  x_pos = x_pos >> b2;
  b1 = !(!(x_pos >> 1));

  return ((b16+b8+b4+b2+b1+2)&zero_mask) | is_zero;
}
